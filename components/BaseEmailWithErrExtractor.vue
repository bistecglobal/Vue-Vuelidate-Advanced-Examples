<template>
  <form-group :validator="v" label="Email">
    <input type="email" class="input" v-model="email" placeholder="user@yahoo.com" @input="v.$touch()">
  </form-group>
</template>

<script>
export default {
  props: {
    value: {
      type: String,
      default: ""
    },

    v: {
      type: Object,
      required: true
    }
  },

  computed: {
    email: {
      get() {
        return this.value;
      },

      set(value) {
        this.$emit("input", value);
      }
    }
  }
};
</script>
